interface QuestionSeed {
  id: string
  legacyId?: string
  questionNumber: number
  field: FieldName
  subField: string
  questionText: string
  choices: [string, string, string, string]
  correctAnswer: ChoiceKey
  correctReason: string
  wrongReasons: Partial<Record<ChoiceKey, string>>
  points: string[]
  keywords: string[]
  quoted?: boolean
  sourceName?: string
}

const keys: ChoiceKey[] = ['ア', 'イ', 'ウ', 'エ']

const createQuestion = (seed: QuestionSeed): Question => {
  const choices: Choice[] = seed.choices.map((text, index) => ({ key: keys[index], text }))
  const correctChoice = choices.find(choice => choice.key === seed.correctAnswer)
  return {
    id: seed.id,
    legacyIds: seed.legacyId ? [seed.legacyId] : undefined,
    examYear: seed.quoted ? 2024 : 2026,
    examSeason: seed.quoted ? '秋期' : '春期',
    examType: 'morning',
    questionNumber: seed.questionNumber,
    field: seed.field,
    subField: seed.subField,
    questionText: seed.questionText,
    choices,
    correctAnswer: seed.correctAnswer,
    officialAnswerText: `${seed.correctAnswer}：${correctChoice?.text ?? ''}`,
    sourceName: seed.quoted ? `情報処理推進機構（IPA） 応用情報技術者試験 令和6年度 秋期 午前 問${seed.questionNumber}` : (seed.sourceName ?? 'AP Study original'),
    sourceUrl: seed.quoted ? ipaPastQuestionUrl : syllabusUrl,
    isQuoteFromIpa: Boolean(seed.quoted),
    explanation: {
      correctReason: seed.correctReason,
      wrongReasons: seed.wrongReasons,
      points: seed.points,
      keywords: seed.keywords,
      isAiGenerated: false,
    },
  }
}


interface V180QuestionSeed {
  id: string
  questionNumber: number
  field: FieldName
  subField: string
  questionText: string
  choices: [string, string, string, string]
  correctAnswer: ChoiceKey
  choiceReasons: [string, string, string, string]
  points: string[]
  keywords: string[]
}

const createV180Seed = (seed: V180QuestionSeed): QuestionSeed => {
  const answerOffset = [...seed.id].reduce((total, character) => total + character.charCodeAt(0), 0) % keys.length
  const rotate = <T,>(values: [T, T, T, T]): [T, T, T, T] => keys.map((_, index) => values[(index - answerOffset + keys.length) % keys.length]) as [T, T, T, T]
  const choices = rotate(seed.choices)
  const choiceReasons = rotate(seed.choiceReasons)
  const correctAnswer = keys[(keys.indexOf(seed.correctAnswer) + answerOffset) % keys.length]
  return {
    id: seed.id,
    questionNumber: seed.questionNumber,
    field: seed.field,
    subField: seed.subField,
    questionText: seed.questionText,
    choices,
    correctAnswer,
    correctReason: choiceReasons[keys.indexOf(correctAnswer)],
    wrongReasons: Object.fromEntries(keys.map((key, index) => [key, choiceReasons[index]])),
    points: seed.points,
    keywords: seed.keywords,
    sourceName: 'AP Study original',
  }
}

